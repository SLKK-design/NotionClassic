const EmojiSprite *emoji_find(const char *utf8) {
    int lo = 0, hi = EMOJI_COUNT - 1;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        int cmp = strcmp(EMOJI_TABLE[mid].utf8, utf8);
        if (cmp == 0) return &EMOJI_TABLE[mid];
        if (cmp < 0)  lo = mid + 1;
        else          hi = mid - 1;
    }
    return NULL;
}
